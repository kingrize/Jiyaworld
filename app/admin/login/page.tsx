/* app/admin/login/page.tsx */
"use client";

import { useState, useEffect } from "react";
import { useRouter } from "next/navigation";
import {
    signInWithPopup,
    signInWithEmailAndPassword,
    GithubAuthProvider,
    GoogleAuthProvider,
    onAuthStateChanged,
} from "firebase/auth";
import { auth } from "@/lib/firebase";

// Icons (inline SVGs for minimal dependencies)
const GithubIcon = () => (
    <svg width="18" height="18" viewBox="0 0 24 24" fill="currentColor">
        <path d="M12 0C5.37 0 0 5.37 0 12c0 5.31 3.435 9.795 8.205 11.385.6.105.825-.255.825-.57 0-.285-.015-1.23-.015-2.235-3.015.555-3.795-.735-4.035-1.41-.135-.345-.72-1.41-1.23-1.695-.42-.225-1.02-.78-.015-.795.945-.015 1.62.87 1.845 1.23 1.08 1.815 2.805 1.305 3.495.99.105-.78.42-1.305.765-1.605-2.67-.3-5.46-1.335-5.46-5.925 0-1.305.465-2.385 1.23-3.225-.12-.3-.54-1.53.12-3.18 0 0 1.005-.315 3.3 1.23.96-.27 1.98-.405 3-.405s2.04.135 3 .405c2.295-1.56 3.3-1.23 3.3-1.23.66 1.65.24 2.88.12 3.18.765.84 1.23 1.905 1.23 3.225 0 4.605-2.805 5.625-5.475 5.925.435.375.81 1.095.81 2.22 0 1.605-.015 2.895-.015 3.3 0 .315.225.69.825.57A12.02 12.02 0 0024 12c0-6.63-5.37-12-12-12z" />
    </svg>
);

const GoogleIcon = () => (
    <svg width="18" height="18" viewBox="0 0 24 24">
        <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
        <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
        <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
        <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
    </svg>
);

const MailIcon = () => (
    <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
        <rect x="2" y="4" width="20" height="16" rx="2" />
        <path d="m22 7-8.97 5.7a1.94 1.94 0 0 1-2.06 0L2 7" />
    </svg>
);

const LoadingSpinner = () => (
    <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" className="admin-login-spinner">
        <path d="M21 12a9 9 0 1 1-6.219-8.56" />
    </svg>
);

export default function AdminLoginPage() {
    const router = useRouter();
    const [email, setEmail] = useState("");
    const [password, setPassword] = useState("");
    const [error, setError] = useState<string | null>(null);
    const [loading, setLoading] = useState(false);
    const [checkingAuth, setCheckingAuth] = useState(true);

    // Redirect if already authenticated
    useEffect(() => {
        const unsubscribe = onAuthStateChanged(auth, (user) => {
            if (user) {
                router.replace("/admin");
            } else {
                setCheckingAuth(false);
            }
        });
        return () => unsubscribe();
    }, [router]);

    const handleError = (err: unknown) => {
        const errorCode = (err as { code?: string })?.code;
        const errorMessages: Record<string, string> = {
            "auth/invalid-email": "Invalid email address.",
            "auth/user-disabled": "This account has been disabled.",
            "auth/user-not-found": "No account found with this email.",
            "auth/wrong-password": "Incorrect password.",
            "auth/invalid-credential": "Invalid credentials. Please try again.",
            "auth/too-many-requests": "Too many attempts. Please try again later.",
            "auth/popup-closed-by-user": "Sign-in popup was closed.",
            "auth/account-exists-with-different-credential": "An account already exists with this email.",
        };
        setError(errorMessages[errorCode || ""] || "Authentication failed. Please try again.");
    };

    const signInWithGitHub = async () => {
        setLoading(true);
        setError(null);
        try {
            const provider = new GithubAuthProvider();
            await signInWithPopup(auth, provider);
            router.replace("/admin");
        } catch (err) {
            handleError(err);
        } finally {
            setLoading(false);
        }
    };

    const signInWithGoogle = async () => {
        setLoading(true);
        setError(null);
        try {
            const provider = new GoogleAuthProvider();
            await signInWithPopup(auth, provider);
            router.replace("/admin");
        } catch (err) {
            handleError(err);
        } finally {
            setLoading(false);
        }
    };

    const signInWithEmail = async (e: React.FormEvent) => {
        e.preventDefault();
        if (!email || !password) {
            setError("Please enter both email and password.");
            return;
        }
        setLoading(true);
        setError(null);
        try {
            await signInWithEmailAndPassword(auth, email, password);
            router.replace("/admin");
        } catch (err) {
            handleError(err);
        } finally {
            setLoading(false);
        }
    };

    // Show nothing while checking auth state
    if (checkingAuth) {
        return (
            <main className="admin-login-page">
                <div className="admin-login-loading">Checking authentication...</div>
            </main>
        );
    }

    return (
        <main className="admin-login-page">
            <div className="admin-login-container">
                {/* Header */}
                <header className="admin-login-header">
                    <h1 className="admin-login-title">Admin Login</h1>
                    <p className="admin-login-subtitle">
                        Sign in to manage mods and releases
                    </p>
                </header>

                {/* Error message */}
                {error && (
                    <div className="admin-login-error">
                        <span>{error}</span>
                    </div>
                )}

                {/* OAuth buttons */}
                <div className="admin-login-oauth">
                    <button
                        type="button"
                        className="admin-login-btn admin-login-btn--github"
                        onClick={signInWithGitHub}
                        disabled={loading}
                    >
                        {loading ? <LoadingSpinner /> : <GithubIcon />}
                        <span>Continue with GitHub</span>
                    </button>
                    <button
                        type="button"
                        className="admin-login-btn admin-login-btn--google"
                        onClick={signInWithGoogle}
                        disabled={loading}
                    >
                        {loading ? <LoadingSpinner /> : <GoogleIcon />}
                        <span>Continue with Google</span>
                    </button>
                </div>

                {/* Divider */}
                <div className="admin-login-divider">
                    <span>or continue with email</span>
                </div>

                {/* Email form */}
                <form className="admin-login-form" onSubmit={signInWithEmail}>
                    <div className="admin-login-field">
                        <label htmlFor="email" className="admin-login-label">
                            Email
                        </label>
                        <input
                            id="email"
                            type="email"
                            value={email}
                            onChange={(e) => setEmail(e.target.value)}
                            className="admin-login-input"
                            placeholder="admin@example.com"
                            autoComplete="email"
                            disabled={loading}
                        />
                    </div>
                    <div className="admin-login-field">
                        <label htmlFor="password" className="admin-login-label">
                            Password
                        </label>
                        <input
                            id="password"
                            type="password"
                            value={password}
                            onChange={(e) => setPassword(e.target.value)}
                            className="admin-login-input"
                            placeholder="••••••••"
                            autoComplete="current-password"
                            disabled={loading}
                        />
                    </div>
                    <button
                        type="submit"
                        className="admin-login-btn admin-login-btn--submit"
                        disabled={loading}
                    >
                        {loading ? <LoadingSpinner /> : <MailIcon />}
                        <span>Sign in</span>
                    </button>
                </form>

                {/* Footer note */}
                <p className="admin-login-footer">
                    This login is for authorized administrators only.
                </p>
            </div>
        </main>
    );
}
